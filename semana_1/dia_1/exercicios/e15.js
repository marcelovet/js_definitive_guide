/**
 * ### **Exercício 15 - Operadores de Atribuição Compostos**
 * Use +=, -=, \*= e /= para modificar uma variável numérica.
 */
let a = 0;
console.log(`I'm a ${a}`);
a += 5;
console.log(`Now I summed 5 and will be ${a}`);
a -= 2;
console.log(`Now I subtract 2 and will be ${a}`);
a *= 3;
console.log(`Now I'm the product by 3 and will be ${a}`);
a /= 2;
console.log(`Now I'm the fraction by 2 and will be ${a.toFixed(2)}`);
